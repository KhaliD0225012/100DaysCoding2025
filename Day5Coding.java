package day5coding;

public class Day5Coding {

    public static void main(String[] args) {
        
        byte A = 127;
        short B = 32000;
        int C = 170000000;
        long D = 900000000;
        
        System.out.printf("Byte: %d\n", A);
        System.out.printf("Short; %d\n", B);
        System.out.printf("int: %d\n", C);
        System.out.printf("long; %d\n", D);
        
    }
    
}
