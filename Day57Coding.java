package day57coding;

public class Day57Coding {

    public static void main(String[] args) {
        
        int i = 0;
        while (i < 10){
            for (int j = 0; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println("");
            i++;
        }
        
    }
    
}
