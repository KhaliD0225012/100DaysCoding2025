package day4coding;

public class Day4Coding {
	
	public static void main(String[] args) {
		
		// newline (/n), menambahkan baris baru setelah mencetak
		System.out.printf("\nNama: %s\nNIM: %s\n", "Khalid", "DO225012");
		
		//space (/s), menambahkan spasi setelah mencetak
		System.out.println("Fakultas:\s", "Teknik");
		
		//tab (/t), menambah tab atau spasi lebar setelah mencetak
		System.out.println("Prodi:\t", "Informatika");
		
		//backspace (/b), menghapus satu variable/karakter sebelumnya
		System.out.println("Kelas: BE\b");
	}
}
